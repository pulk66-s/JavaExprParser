package Context;

/**
 * @brief   This enum is used to represent the status code of the parsing
 * @details This enum is used to represent the status code of the parsing
 *          It contains the following values:
 *          - SUCCESS: The parsing was successful
 *          - FAILURE: The parsing failed
 *          - ERROR:   An error occured during the parsing
 */
public enum StatusCode {
    SUCCESS,
    FAILURE,
    ERROR,
}
